def draw_board(board):
    window.fill(hp.GREY)
    for row in range(hp.ROW + 1):
        for col in range(hp.COL + 1):
            if row != hp.ROW and col != hp.COL:
                pygame.draw.rect(
                    window, 
                    hp.BACKGROUND_COLOR, 
                    (
                        (row + 1) * (hp.GRID_SIZE + 1), 
                        (col + 1) * (hp.GRID_SIZE + 1),
                        hp.GRID_SIZE,
                        hp.GRID_SIZE
                    )
                )
            ## black
            if board[col][row] == 1:
                pygame.draw.circle(
                window, 
                hp.BLACK, 
                (
                    (row + 1) * (hp.GRID_SIZE + 1), 
                    (col + 1) * (hp.GRID_SIZE + 1)
                ),
                hp.STONE_SIZE
            )

            ## white
            if board[col][row] == 2:
                pygame.draw.circle(
                window, 
                hp.WHITE, 
                (
                    (row + 1) * (hp.GRID_SIZE + 1), 
                    (col + 1) * (hp.GRID_SIZE + 1)
                ),
                hp.STONE_SIZE
            )

main()